/**
 * Generates src/main/bug-relay-config.ts from env vars at build time.
 *
 * This file is gitignored — the URL + shared secret for the bug-report
 * relay should never land in the repo. Instead they're injected at build
 * time from env vars (locally) or GitHub Actions secrets (CI / release).
 *
 * If the env vars aren't set, we write an empty stub so tsc still has a
 * valid module to import. The app then falls back to "Open GitHub
 * issue" mode since BUG_RELAY_URL is empty.
 */
const fs = require('fs');
const path = require('path');

const URL = process.env.BUG_RELAY_URL || '';
const SECRET = process.env.BUG_RELAY_SECRET || '';

const target = path.join(__dirname, '..', 'src', 'main', 'bug-relay-config.ts');

// If a real config already exists locally AND no env vars were passed,
// preserve it — dev workflow: developer put their own secrets in the
// file and we don't want to wipe them on every `npm run build`.
if (!URL && !SECRET && fs.existsSync(target)) {
  const existing = fs.readFileSync(target, 'utf8');
  if (/BUG_RELAY_URL\s*=\s*'[^']+'/.test(existing)) {
    process.stdout.write('[bug-relay-config] keeping existing local config\n');
    process.exit(0);
  }
}

const banner = `/**
 * Auto-generated by scripts/write-bug-relay-config.js.
 * Do not edit manually for production — values come from env vars:
 *   BUG_RELAY_URL, BUG_RELAY_SECRET
 * Gitignored. See bug-relay-config.example.ts for the shape.
 */
`;

const body = `${banner}export const BUG_RELAY_URL = ${JSON.stringify(URL)};\nexport const BUG_RELAY_SECRET = ${JSON.stringify(SECRET)};\n`;

fs.writeFileSync(target, body, 'utf8');
process.stdout.write(
  URL
    ? `[bug-relay-config] wrote config with BUG_RELAY_URL=${URL}\n`
    : '[bug-relay-config] wrote empty stub (no env vars set)\n'
);
